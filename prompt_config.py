def get_physics_prompt(target_subject_name, target_theme, target_problem):
    """
    AIに渡すプロンプトを生成する共通関数
    """
    return f"""
以下の条件に従って，大学生向けの物理の問題と解説を作成してください。

【設定パラメータ】
- 分野：{target_subject_name}
- テーマ：{target_theme}
- タイトル：{target_problem['title']}
- 難易度：{target_problem['difficulty']}
- 問題の設定・条件：{target_problem['setup']}

【出力要件（厳守）】
1. メタデータ：ファイルの先頭に、必ず以下の形式で難易度タグ（Frontmatter）を出力すること。
---
tags:
  - {target_problem['difficulty']}
---
2. ストーリー性：単なる数式処理ではなく，指定された設定に基づき，具体的な物理現象を一連の流れを持って解き進める構成にすること。ただし簡単すぎる問題や考察だけの問題はさけ，計算や導出も含むようにすること。
3. 誘導形式：問題は (1), (2), (3)... と順を追って誘導する形式にすること。前の小問の結果を使って次のステップへ進む論理的な流れを作ること。ただし問題の設問個数は問わない。

4. 数式のフォーマット（超重要）：
   - 文中の短い数式（インライン）は `$F=ma$` のように `$` で囲むこと。
   - 運動方程式の立式，重要な導出ステップ，最終的な解答などの「独立した数式（ディスプレイ数式）」は，必ず以下のように `$$` を単独の行に置き，改行して数式を挟むこと。
     （良い例）
     文章...

     $$
     F = ma
     $$
     
     文章...
   - `$$F=ma$$` のように1行にまとめて出力することは絶対に避けてください。
   - 複数行の数式（align環境など）で改行を行う場合は，Markdownの干渉を防ぐため必ず `\\\\` （バックスラッシュ4つ）を使用すること。

5. 図の不使用：図を用いず，文章のみで系の設定（配置，座標系の定義，力の向きなど）が学習者に100%誤解なく伝わるように厳密に記述すること。
6. 余計な挨拶や説明は一切省き，Markdownのテキストのみを直接出力してください。句読点は「。」「，」を利用し，「、」を用いないこと。問題文は常態（である調），解答は敬体（ですます調）で丁寧な説明を加えつつも簡潔に記述すること。
7. 問題文と解説の両方において「」や（）といった括弧類を多用しないこと。特に問題文では、括弧を使わずに文章だけで条件や設定を伝える工夫をしてください。
8. 華美な修飾表現をしないこと。「美しい」や「完璧に記述する」などの表現は避け，落ち着いた表現で，学習者が理解しやすい文章を心がけること。
9. その他，LaTeX上の記法としてベクトルは\\mathbfを使うのではなく，\\boldsymbolを使用すること。
10. 解説では計算過程を初学の方でも理解できるように丁寧に記述すること。計算式の導出過程を省略せず，なぜその式が成り立つのかを文章で説明すること。特に積分では「積分すると」とだけ書いて結果へ飛ばず，積分変数と積分範囲，線素・面積要素・体積要素，置換積分で用いる変数変換，原始関数，端点の代入を明示すること。ベクトル解析では内積・外積の成分計算と，対称性によって消える成分も式で示すこと。複数段の計算は `aligned` 環境を用い，立式から最終結果まで式変形を追えるようにすること。
11. 設問のまとまり：設問数を増やすことで計算量を確保しないこと。密接に関係する短い問いは一つの設問へまとめ，原則として3問から4問程度の設問で一つの物理的な流れを完成させること。各設問では立式，計算，結果の確認や解釈までをある程度まとまって行わせること。
12. 既知の法則の提示：その問題で応用させたい法則や構成方程式は，名称だけを突然示さず，必要な数式と各記号の意味を問題文中で与えること。法則そのものの導出が主題である場合に限り，誘導を付けたうえで導出させること。「オームの法則を用いよ」などの曖昧な指示だけで済ませないこと。
13. 問題設定の丁寧さ：座標系，正方向，物理量の定義，近似，境界条件を計算前に明記すること。学習者が何を出発点として何を計算すべきか判断できるようにしつつ，最終結果そのものは与えないこと。
14. 求める量の明示：各設問では「電位差を求めよ」のような曖昧な指示を避け，例えば「内側導体と外側導体の電位差を $V=V_{{\\mathrm{{in}}}}-V_{{\\mathrm{{out}}}}$ と定義し，$V$ を求めよ」のように，求める物理量の記号，順序，符号，成分，評価位置を問題文中で定義すること。
15. 解の一意性：与えられた条件だけで答えが一意に定まることを必ず確認すること。電位には基準，場には領域と境界条件，力や流束には正方向，回路には電源の接続条件を明記すること。説明問題を含める場合も，先に一意に計算できる量を求めさせ，その結果に基づいて説明させること。
16. 見出し階層：解説の各設問には `### (1)`，`### (2)` の3階層見出しを用いること。設問内をさらに区切る場合は `####` 見出しを使用せず，`**電位の導出**` のような独立した太字ラベルを置き，その前後に空行を入れること。
17. 法則ごとの扱い：ポアソン方程式とラプラス方程式は問題文に既知の式として提示し，導出そのものではなく領域ごとの使い分けと境界値問題を問うこと。局所的なオームの法則はドルーデ模型など必要な微視的仮定を与えれば導出を問うてもよい。ビオ・サバールの法則はベクトルポテンシャルと必要なベクトル解析の関係を与えれば導出を問うてもよい。導出を問う場合も，出発式と記号を問題文中に明示すること。

【出力フォーマット】
---
tags:
  - {target_problem['difficulty']}
---

# {target_problem['title']}

## 問題
（具体的な物理系の設定と導入文）
(1) ...
(2) ...

---

## 解説
（各小問の解答と、それが意味する物理的な解釈を詳細に記述）

### (1) ...
（解答と解説）

### (2) ...
（解答と解説）
"""
